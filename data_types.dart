void main() {
  // Integer (int) data type
  int age = 21;
  print('Age: $age');

  //Double data type
  double price = 10.55;
  print('Price is: $price');

  // String data type
  String name = 'Brenda Kinyanjui';
  print('Name: $name');

  //List data type
  List<String> towns = ['Nairobi', 'Kisumu', 'Mombasa', 'Eldoret'];
  print('The major airports are located in $towns');

  //Map data type
  Map<String, String> capitals = {'Kenya': 'Nairobi', 'Tanzania': 'Daresalaam'};
  print('Capital cities: $capitals');
}
